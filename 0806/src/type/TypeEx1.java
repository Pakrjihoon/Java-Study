package type;

public class TypeEx1 {
	
	public static void main(String[] args) {
			
		/* 
		 * 자바의 자료형
		 * - 기본자료형
		 * - 객체자료형
		 * 
		 * - 기본자료형 
		 * - boolean
		 * - char
		 * - byte
		 * - short
		 * - int
		 * - long
		 * - double
		 * - float
		 * 
		 * 변수의 선언
		 * 자료형 변수명;
		 */
		// 기본 자료형 boolean 형
		// 지역변수 b를 선언하고 
		// true 값으로 대입한다.(초기화)
		boolean b=true;
		
		// 기본자료형 int 형
		// 지역변수 i를 선언하고
		// 값 3을 대입
		int i = 3;
		
		
		//출력
		System.out.println(i);
	
	} //main() end

} //TypeEx1 end
