package type;

public class TypeEx4 {

	public static void main(String[] args) {
		
		/* 
		 * float 
		 * - 크기 : 4byte 
		 * - 기본값 : 0.0f
		 * 
		 * double
		 * - 자바의 기본실수
		 * - 크기 : 8byte
		 * - 기본값 : 0.0;
		 *  
		 */
		
		
		// 기본자료형 float형
		// 지역변수 f를 선언하고
		// 3.14로 초기화 
		float f = 3.14f;
		System.out.println(f);
		
	} // main() end

} // TypeEx4 end
