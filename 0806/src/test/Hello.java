package test;

// 클래스명의 첫 글자는 무조건 대문자로 지정한다.
public class Hello {
	public static void main(String[] args) {
		
		// 콘솔에 출력
		System.out.println("Hello Java !");
		
	} //main() end
} // Hello end


/* 
 Hello 클래스를 선언하며 
 그 안에는 main 메서드가 포함되어 있다. 
 main 메서드 안에 String[] args 에서 args 는 변수명이다.
*/
 
